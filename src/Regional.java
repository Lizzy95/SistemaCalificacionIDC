    public String[][] ordenar(String[][] junior, Integer valJun){
        String aux;
            Integer aux1,aux2;
            for(int i = 0; i< valJun-1; i++){
                for(int j=0; j < valJun-1-i; j++){
                    aux1 = Integer.parseInt(junior[j+1][5]);
                    aux2 = Integer.parseInt(junior[j][5]);
                    if(aux1 > aux2){
                        aux = junior[j+1][0];
                        junior[j+1][0] = junior[j][0];
                        junior[j][0] = aux;
                        
                        aux = junior[j+1][1];
                        junior[j+1][1] = junior[j][1];
                        junior[j][1] = aux;
                        
                        aux = junior[j+1][2];
                        junior[j+1][2] = junior[j][2];
                        junior[j][2] = aux;
                        
                        aux = junior[j+1][3];
                        junior[j+1][3] = junior[j][3];
                        junior[j][3] = aux;

                        aux = junior[j+1][4];
                        junior[j+1][4] = junior[j][4];
                        junior[j][4] = aux;
                        
                        aux = junior[j+1][5];
                        junior[j+1][5] = junior[j][5];
                        junior[j][5] = aux;
                    }
                } 
            }
            return junior;
    }
    
